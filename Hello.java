public class Hello{

    public static void main(String[] args) {
      System.out.println( "<!doctype html>\n" );
      System.out.println ("<!doctype html>\n" );
      System.out.println( "<html lang='en'>\n" );

      System.out.println( "<head>\n" );
      System.out.println( "<meta charset='utf-8'>\n" );
      System.out.println( "<title>Hello World</title>\n" );
      System.out.println( "</head>\n\n" );

      System.out.println( "<body>\n" );
      System.out.println( "<h1>Hello World !</h1>\n" );
      System.out.println( "</body>\n\n" );

      System.out.println( "</html>" );
    }
  }